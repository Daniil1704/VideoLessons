                sb_result.AppendLine(" 00:00:00 - Начало урока");
                sb_result.AppendLine(" 00:01:00 - Субтитры в процессе создания..");
                return sb_result.ToString();
            }


            
        }


       
        public static String convertMsToHMmSs(string path, string filename)
        {

            string s1 = Path.Combine(path, "video", filename);

            if (File.Exists(s1))
            {
                IEnumerable<MetadataExtractor.Directory> directories =
                ImageMetadataReader.ReadMetadata(s1);

                var subIfdDirectory =
                    directories?.OfType<MetadataExtractor.Formats.QuickTime.QuickTimeMovieHeaderDirectory>().FirstOrDefault();

                var dateTime =
                    subIfdDirectory?.GetDescription(MetadataExtractor.Formats.QuickTime.QuickTimeMovieHeaderDirectory.TagDuration);

                if (dateTime != null)
                {
                    int index = dateTime.IndexOf(".");
                    return (index > 0 ? dateTime.Substring(0, index) : dateTime);
                }
                else
                {
                    return "00:01:00";
                }
            }
            else
            {
                return "00:01:00";
            }
            
        }

        public static string RecursiveHtmlDecode(string str)
        {
            if (string.IsNullOrWhiteSpace(str)) return str;
            var tmp = HttpUtility.HtmlDecode(str);
            while (tmp != str)
            {
                str = tmp;
                tmp = HttpUtility.HtmlDecode(str);
            }
            return str;
        }


        /// <summary>
        /// return the detected encoding and the contents of the file.
        /// </summary>
        /// <param name="fileName"></param>
        /// <param name="contents"></param>
        /// <returns></returns>
        public static Encoding DetectEncoding(String fileName, out String contents)
        {
            // open the file with the stream-reader:
            using (StreamReader reader = new StreamReader(fileName, true))
            {
                // read the contents of the file into a string
                contents = reader.ReadToEnd();

                // return the encoding.
                return reader.CurrentEncoding;
            }
        }

