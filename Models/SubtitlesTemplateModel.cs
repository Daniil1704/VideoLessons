        public static string GetFromFile(string path, string name)
        {

            StringBuilder sb_result = new StringBuilder();
  
            string s1 = Path.Combine(path, "video", "sub", name);

            if (File.Exists(s1))
            {

                string from_t = File.ReadAllText(s1, CodePagesEncodingProvider.Instance.GetEncoding(1251));

                if (from_t.Trim() == "")
                {
                    sb_result.AppendLine(" 00:00:00 - Начало урока");
                    sb_result.AppendLine(" 00:01:00 - Субтитры в процессе создания..");
                }
                else
                {
                    //(!Regex.IsMatch(from_t, "^[А-Яа-я]+$"))
                    if ((!from_t.Contains("а")) && (!from_t.Contains("б")) && (!from_t.Contains("о")))
                    {
                        //возможно, ошибка в кодировке
                        //пробуем utf8
                        sb_result.AppendLine(" 00:00:00 - Кодировка UTF-8");
                        from_t = File.ReadAllText(s1, System.Text.Encoding.UTF8);
                    }

                    if ((!from_t.Contains("а")) && (!from_t.Contains("б")) && (!from_t.Contains("о")))
                    {
                        //неизвестная кодировка
                        sb_result.AppendLine(" 00:00:00 - Начало урока");
                        sb_result.AppendLine(" 00:01:00 - Ошибка в кодировкке..");
                    }
                    else
                    {
                        string[] from_arr = from_t.Split(
                                            new[] { "\r\n", "\r", "\n" },
                                            StringSplitOptions.None
                                        );

                        foreach (string s in from_arr)
                        {
                            try
                            {
                                bool istruetime = Regex.IsMatch(s,
                                      @"^[0-9]{1}[0-9:]{2}[0-9]{1}[0-9:]{2}[0-9]{1,2} [-]{1}[^\r\n]*$",
                                    RegexOptions.IgnoreCase, TimeSpan.FromMilliseconds(50));
                                if (istruetime)
                                {
                                    string s_temp = RecursiveHtmlDecode(s);
                                    if (s_temp.Contains("\"")) { s_temp = s_temp.Replace("\"", "'"); }
                                    if (s_temp.Trim() != "") { sb_result.AppendLine(s_temp); }
                                }
                                else
                                {
                                    //sb_result.AppendLine(" 00:00:00 - СТРОКА С ОШИБКОЙ");
                                }
                            }
                            catch (RegexMatchTimeoutException)
                            {
                                //при ошибке - пропускаем строку
                            }
                        }
                    }
                }
            }
            else
            {
                sb_result.AppendLine(" 00:00:00 - Начало урока");
                sb_result.AppendLine(" 00:01:00 - Субтитры в процессе создания..");
            }

            if (sb_result.ToString().Length>2)
